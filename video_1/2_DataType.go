package main

import "fmt"

func main() {
	// Number
	fmt.Println("Integer = ", 1)
	fmt.Println("Float = ", 22.34)

	//	Boolean
	fmt.Println("True = ", true)
	fmt.Println("False = ", false)

	//	String
	fmt.Println("Manchester is Red")
	fmt.Println("1 + 1")

	// String Function
	fmt.Println(len("Manchester is Red")) //count the number of characters
	fmt.Println("Manchester is red"[3])   //take one of the characters in byte
}
